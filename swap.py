# program to swap values of two variables in long method
a=int(input("Enter value of a: "))
b=int(input("Enter value of b: "))
print("before swap a & b are respectively",a,b)
c=a # c is called an auxillary variable or intermediate variable
a=b # a value updated or got changed
b=c # b value is
print("After swap a & b are respectively",a,b)