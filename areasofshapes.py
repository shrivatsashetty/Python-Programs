def areasquare(l):
    print("area of the square:",l**2)
    return l**2

def arearect(l=0.0,b=0.0):
    print("area of rectangle:",l*b)
    return l*b

def areacircle(r):
    area = 3.14*r**2
    print("area of circle:",area)
    return area

def areatriangle(b,h):
    area = 0.5*(b*h)
    print("Area of the traingle:",area)
    return area
   
   
   
   
